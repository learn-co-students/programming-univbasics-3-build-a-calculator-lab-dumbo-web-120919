def addition(num1, num2)
  sum = num1 + num2
  return sum
end

def subtraction(num1, num2)
  difference = num1 - num2
  return difference
end

def division(num1, num2)
  dividend = num1/num2
  return dividend
end

def multiplication(num1, num2)
  product = num1 * num2
  return product
end

def modulo(num1, num2)
  answer = num1 % num2
  return answer
end

def square_root(num)
  root = Math.sqrt(num)
  return root
end
